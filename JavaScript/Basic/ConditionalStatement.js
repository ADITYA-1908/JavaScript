//if else
let a = 10;
if (a > 6) {
    console.log("Greater");
}
else {
    console.log("smaller");
}

//if elseif else
let b = 10;
if (b > 7) {
    console.log("Greater than 7");
}
else if (b < 7) {
    console.log("smaller than 7");
}
else {
    console.log("equal to 10");
}

